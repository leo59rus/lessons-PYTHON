def find_farthest_orbit(list_of_orbits):
    area = 0 #наибольшая площадь
    ind = 0  #индекс наибоьшей площади
    for i, (a, b) in enumerate(list_of_orbits):
        if a * b > area and a != b:
            ind = i
            area = a * b
    return list_of_orbits[ind]


